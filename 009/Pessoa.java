class Pessoa {
    
    private String nome;
    private String cpf;
    
    protected void setNome(String nome) {
        this.nome = nome;
    }
    
    protected String getNome() {
        return this.nome;
    }
    
    protected void setCpf(String cpf) {
        this.cpf = cpf;
    }
    
    protected String getCpf() {
        return this.cpf;
    }
    
    
}